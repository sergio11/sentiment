package models;

import java.io.Serializable;

/**
 *
 * @author sergio
 */
public class Topic implements Serializable{
    
    private Long id;
    private String name;

    public Topic(String name) {
        this.name = name;
    }
    
   
    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
}
